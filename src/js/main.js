let ipadHover = document.querySelector(".ipad-project");
ipadHover.onmouseover = function() {
  let nameOfTheSite = document.querySelector(".ipad-project-name");
  let buttonOnImg = document.querySelector(".ipad-project-btn");
  let discription = document.querySelector(".ipad-project-discription");
  nameOfTheSite.style.display = "block";
  buttonOnImg.style.display = "flex";
  discription.style.display = "flex";
  ipadHover.onmouseout = function() {
    nameOfTheSite.style.display = "none";
    buttonOnImg.style.display = "none";
    discription.style.display = "none";
  };
};

